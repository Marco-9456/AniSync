package com.anisync.android.widget.designsystem.tokens

import androidx.compose.ui.unit.sp

/**
 * Typography tokens for widget text styles.
 * Centralizes all hardcoded sp values used across widgets.
 */
object WidgetTypography {

    // === Title Sizes ===
    object Title {
        val large = 18.sp
        val medium = 16.sp
        val small = 14.sp
    }

    // === Body Sizes ===
    object Body {
        val large = 14.sp
        val medium = 12.sp
    }

    // === Caption/Label Sizes ===
    object Caption {
        val large = 12.sp
        val medium = 10.sp
    }

    // === Badge Text ===
    val badge = 12.sp
    
    // === Countdown Text ===
    val countdown = 14.sp
}
